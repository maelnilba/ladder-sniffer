const parseMsg = (allMsg) => {
  let returnMsg = {};

  let firstSplit = allMsg.replace(/(\r\n|\n|\r)/gm, "").split(";");
  for (let i = 0; i < firstSplit.length - 1; i++) {
    let sndSplit = firstSplit[i].split("=");
    let id = sndSplit[0].split("[").pop().split("]")[0];
    let msg;
    if (sndSplit[1]) {
      msg = sndSplit[1].trim();
    } else {
      msg = "undefined";
    }

    returnMsg = { ...returnMsg, [msg]: id };
  }
  return returnMsg;
};

